export function searchFoods(query: string): Food[] {
  const lowerQuery = query.toLowerCase()
  
  // Filter first
  const matches = FOODS_DATABASE.filter(food => 
    food.name.toLowerCase().includes(lowerQuery) ||
    food.tags.some(tag => tag.includes(lowerQuery))
  )

  // Sort logic:
  // 1. Healthy (gutStressors: false) comes first
  // 2. Unhealthy (gutStressors: true) comes last
  // 3. Within groups, prioritize exact name matches or starts-with matches
  return matches.sort((a, b) => {
    // Primary sort: Gut health (false < true)
    if (a.gutStressors !== b.gutStressors) {
      return a.gutStressors ? 1 : -1
    }
    
    // Secondary sort: Name starts with query
    const aStarts = a.name.toLowerCase().startsWith(lowerQuery)
    const bStarts = b.name.toLowerCase().startsWith(lowerQuery)
    
    if (aStarts && !bStarts) return -1
    if (!aStarts && bStarts) return 1
    
    // Tertiary sort: Alphabetical
    return a.name.localeCompare(b.name)
  })
}

export function getFoodById(id: string): Food | undefined {
  return FOODS_DATABASE.find(food => food.id === id)
}
